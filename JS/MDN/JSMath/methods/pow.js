// pow.js
// Math.pow(x) returns the result of the pow operation
// The Math.pow() static method returns the value of a base raised to a power.

console.log("Math.pow(2) =", Math.pow(2));
console.log("Math.pow(10) =", Math.pow(10));
console.log("Math.pow(7, 3) =", Math.pow(7, 3));
// Expected output: Math.pow(7, 3) = 343

console.log("Math.pow(4, 0.5) =", Math.pow(4, 0.5));
// Expected output: Math.pow(4, 0.5) = 2

console.log("Math.pow(7, -2) =", Math.pow(7, -2));
// Expected output: Math.pow(7, -2) = 0.020408163265306124
//                  (1/49)

console.log("Math.pow(-7, 0.5) =", Math.pow(-7, 0.5));
// Expected output: Math.pow(-7, 0.5) = NaN

console.log("Math.pow(5, 2) =", Math.pow(5, 2));
