// fround.js
// Math.fround(x) returns the result of the fround operation
console.log("Math.fround(5.5) =", Math.fround(5.5));
// Expected output: Math.fround(5.5) = 5.5

console.log("Math.fround(5.05) =", Math.fround(5.05));
// Expected output: Math.fround(5.05) = 5.050000190734863

console.log("Math.fround(5) =", Math.fround(5));
// Expected output: Math.fround(5) = 5

console.log("Math.fround(-5.05) =", Math.fround(-5.05));
// Expected output: Math.fround(-5.05) = -5.050000190734863

console.log("Math.fround(2) =", Math.fround(2));
console.log("Math.fround(10) =", Math.fround(10));
