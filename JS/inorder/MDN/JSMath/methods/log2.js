// log2.js
// Math.log2(x) returns the result of the log2 operation
// The Math.log2() static method returns the base 2 logarithm of a number.

console.log("Math.log2(2) =", Math.log2(2));
console.log("Math.log2(10) =", Math.log2(10));
console.log("Math.log2(3) =", Math.log2(3));
// Expected output: Math.log2(3) = 1.584962500721156

console.log("Math.log2(2) =", Math.log2(2));
// Expected output: Math.log2(2) = 1

console.log("Math.log2(1) =", Math.log2(1));
// Expected output: Math.log2(1) = 0

console.log("Math.log2(0) =", Math.log2(0));
// Expected output: Math.log2(0) = -Infinity
