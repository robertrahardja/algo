console.log("Math.log10(10) =", Math.log10(10));
console.log("Math.log10(100000) =", Math.log10(100000));
// Expected output: Math.log10(100000) = 5

console.log("Math.log10(2) =", Math.log10(2));
// Expected output: Math.log10(2) = 0.3010299956639812

console.log("Math.log10(1) =", Math.log10(1));
// Expected output: Math.log10(1) = 0

