const arr = [1, 2, 3, 4, 5];
console.log(`arr is : ${arr}`);

//from
const ans = arr.slice(1);
console.log(`ans is : ${ans}`);

//from to but not including
const ans2 = arr.slice(1, 3);
console.log(`ans2 is : ${ans2}`);

//from the back to the end
const ans3 = arr.slice(-3);
console.log(`ans3 is : ${ans3}`);

//from the back to but not including
const ans4 = arr.slice(-4, -1);
console.log(`ans4 is : ${ans4}`);

const ans5 = arr.slice(-4, 0);
console.log(`ans5 is : ${ans5}`);





